public class BitwiseLogicalEx {
	public static void main(String args[]) {
		System.out.println("Logical Operator");
		//checks the both statement
		//And operator
    		System.out.println((5 > 3) && (8 > 5));
		//Or operator  
		System.out.println((5 < 3) || (8 < 5));		
		System.out.println("Bitwise operator");	
		//And operator
		System.out.println((5 > 3) & (8 > 5));
		//Or operator
		System.out.println((5<3) | (8 > 5));
	}
}
		